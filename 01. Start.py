# Create a list of numbers
# For python, list are like array or vector in other languages
listNumber = [1, 2, 3, 4, 5, 6, 7]

# Loop through list elements
for number in listNumber:
    if (number % 2 == 0):
        print(number, " is even")
    else:
        print(number, " is odd")

print("All done")
